    public void musteriGoruntule(){
        
        model.setRowCount(0);
        
        ArrayList<BireyselMusteri> bireyselmusteriler = new ArrayList<BireyselMusteri>();
        
        bireyselmusteriler = bireyseLIslemler.bireyselMusterileriGetir();
        
        if (bireyselmusteriler != null){
            
            for(BireyselMusteri bireyselMusteri: bireyselmusteriler){
                Object[] eklenecek = {bireyselMusteri.getId(),bireyselMusteri.getOdenecekTutar()};
                model.addRow(eklenecek);
            
            }
        
        }
        
    }

