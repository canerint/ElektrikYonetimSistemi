    private void odemeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_odemeActionPerformed
        
        int selectedrow = bireyselMusteriTablosu.getSelectedRow();
        double kalanTutar = 0;
        
        String odenecekTutar = model.getValueAt(selectedrow, 1).toString();
        
        String eskiTutar = odenecekTutarAlani.getText();
        String girTutar = girilenTutarAlani.getText();
        
        
         if (Double.parseDouble(girTutar) < Double.parseDouble(odenecekTutar)){
        
             kalanTutar  = Double.parseDouble(eskiTutar) - Double.parseDouble(girTutar);
            
             kalanTutarAlani.setText(String.valueOf(kalanTutar));
        
             mesajYazisi.setText("Ödenecek " + kalanTutar + " TL borcunuz kaldı...");
    
        }else if (Double.parseDouble(girTutar) == Double.parseDouble(odenecekTutar)){
        
            kalanTutar  = Double.parseDouble(eskiTutar) - Double.parseDouble(girTutar);
            
             kalanTutarAlani.setText(String.valueOf(kalanTutar));
        
             mesajYazisi.setText("Bu tarihe ait ödenecek borcunuz kalmadı...");
        }else if(Double.parseDouble(girTutar) > Double.parseDouble(eskiTutar)){
           
            mesajYazisi.setText("Lütfen borcunuzdan daha yüksek bir tutar girmeyiniz...");
        }
         
        
        
        if(selectedrow == -1){
            if (model.getRowCount() == 0){
                
                mesajYazisi.setText("Tablo şu anda boş.");
            }
            else{
                mesajYazisi.setText("Lütfen ödenecek fatura seçin.");
            }
        }else{
            int id = (int)model.getValueAt(selectedrow, 0);
            
            
            bireyseLIslemler.faturaOde(id,kalanTutar);
            
            musteriGoruntule();
            
            
        }
        
    }//GEN-LAST:event_odemeActionPerformed

