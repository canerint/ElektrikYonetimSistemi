    private void bireyselGuncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bireyselGuncelleActionPerformed
        
        String odenecekTutar = odenecekTutarAlani1.getText();
        
        int selectedrow =  bireyselMusteriTablosu.getSelectedRow();
        
        if(selectedrow == -1){
            if (model1.getRowCount() == 0){
                mesajYazisi.setForeground(Color.red);
                mesajYazisi.setText("Tablo şu anda boş.");
            }
            else{
                mesajYazisi.setForeground(Color.red);
                mesajYazisi.setText("Lütfen değiştirilecek fatura seçin.");
            }
        }else{
            mesajYazisi.setText("");
            int id = (int)model1.getValueAt(selectedrow, 0);
            
            
            adminIslemler.bireyselGuncelle(id,odenecekTutar);
            
            bireyselMusteriGoruntule();
            mesajYazisi.setForeground(Color.green);
            mesajYazisi.setText("Veri güncellendi...");
            
            
        }
    }//GEN-LAST:event_bireyselGuncelleActionPerformed

