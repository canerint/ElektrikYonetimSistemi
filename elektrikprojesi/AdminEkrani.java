    public void bireyselDinamikAra(String bireyselAra){
        
        TableRowSorter<DefaultTableModel> tr1 = new TableRowSorter<DefaultTableModel>(model1);
        
        bireyselMusteriTablosu.setRowSorter(tr1);

        
        
        tr1.setRowFilter(RowFilter.regexFilter(bireyselAra));

    }
    
    public void kurumsalDinamikAra(String bireyselAra){
        

        TableRowSorter<DefaultTableModel> tr2 = new TableRowSorter<DefaultTableModel>(model2);

        kurumsalMusteriTablosu.setRowSorter(tr2);
        
        

        tr2.setRowFilter(RowFilter.regexFilter(bireyselAra));
    }
    
